import lombok.Builder;
import lombok.Data;
import lombok.NoArgsConstructor;

@Data
@Builder
@NoArgsConstructor
@AllArgsConstructor
@JsonIgnoreProperties(ignoreUnknown = true)
public class LikesDto {

    @JsonProperty("nickName")
    private String nickName;

    @JsonProperty("likes")
    private int likes;
}
